myfruitslist = ["apple", "bananna", "cherry"]
print(myfruitslist)
print(type(myfruitslist))

# accessing a list by position
print(myfruitslist[0])
print(myfruitslist[1])
print(myfruitslist[2])

myfruitslist[2] = "orange"
print(myfruitslist)

# creating a turple
myfinalanwerturple = ("apple", "bananna", "pineapple")
print(myfinalanwerturple)
print(type(myfinalanwerturple))

print(myfinalanwerturple[0])
print(myfinalanwerturple[1])
print(myfinalanwerturple[2])

# Defining a dictionary
myfavouritefruitdictionary = {"Akua" : "pineapple",
    "Saanvi" : "bananna", "Paulo" : "apple"
}
print(myfavouritefruitdictionary)
print(type(myfavouritefruitdictionary))

# Accessing dictionary by name
print(myfavouritefruitdictionary["Akua"])
print(myfavouritefruitdictionary["Saanvi"])
print(myfavouritefruitdictionary["Paulo"])
